// declaring variable myAge and assigning it as 34
var myAge = 34;

// changable variable 'earlyYears' with a value of 2
var earlyYears = 2;

earlyYears *= 10.5;

// setting 'laterYears' to myAge minus 2;
var laterYears = myAge - 2;

// multiplying laterYears by 4 and reassigning it
laterYears *= 4;

// Add earlyYears and laterYears together, and store that in a variable named myAgeInDogYears.
var myAgeInDogYears = earlyYears + laterYears;

// 'myName' as a string and converted to lowercase with the .toLowerCase method
var myName = 'Danny';
myName.toLowerCase();

console.log(`My name is ${myName}. I am ${myAge} in human years which is ${myAgeInDogYears} years old in dog years.`)
